int bfs(int tar){
	queue<int> q;
	vector<int> dist(301,-1);
	dist[10] = 0;
	q.push(10);
	while(q.size()){
		int t = q.front();
		q.pop();

		if(t == tar) return dist[tar];

		for(int i = 0 ; i < 8 ; i++){
			int x = d[i] + t;
			x = max(x,10);
			x = min(x,300);

			if(dist[x] != -1) continue;
			dist[x] = dist[t] + 1;
			q.push(x);
		}

	}
	return dist[tar];
}

