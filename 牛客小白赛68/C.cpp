#define se second
#define endl '\n'
using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 5010;
int n,m;
int a[26][N];
void solve(){
	string s;
	cin >> n >> s;
	s = '?' + s;

	for(int i = 1 ; i <= n ; i++){
		for(int j = 0 ; j < 26 ; j++)
			a[j][i] = a[j][i - 1];
		a[s[i] - 'a'][i]++;
	}
	double ans = 0;

	for(int i = 1 ; i <= n ; i++)
		for(int j = 1 ; j <= n ; j++){
			int r = 0;
			for(int z = 0 ; z < 26 ; z++){
				int c = a[z][j] - a[z][i - 1];
				r += c * (c - 1) / 2;
			}
			ans = max(ans,r * 1.0 / (j - i + 1));
		}

	cout << fixed << setprecision(6) << ans << endl;

	
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	cin >> T;
	while(T--) solve();
	return 0;
}

