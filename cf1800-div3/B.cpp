const int N = 1000010;

void solve(){
    int n, k;
    cin >> n >> k;
    
    int lo[26] {}, up[26] {};
    string s;
    cin >> s;
    
    for (auto c : s) {
        if (islower(c)) {
            lo[c - 'a'] += 1;
        } else {
            up[c - 'A'] += 1;
        }
    }
    
    int max = 0;
    int ans = 0;
    for (int i = 0; i < 26; i++) {
        ans += min(lo[i], up[i]);
        max += (lo[i] + up[i]) / 2;
    }
    
    ans = min(ans + k, max);
    cout << ans << "\n";
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	cin >> T;
	while(T--) solve();
	return 0;
}

