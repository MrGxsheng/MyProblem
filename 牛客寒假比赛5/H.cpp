typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
LL price[N];
LL cnt[N];
void solve(){
	LL x, y, k, n, t;
	cin >> x >> y >> k >> n >> t;
	LL now = 0;
	LL res = 0;
	LL ans = -1;
	for ( LL i = n; i >= 1; i -- ) {
		if (now >= k) {
			LL cnt = now / k;
			now -= cnt * k;
			x += cnt * y;
		}
		now += i;
		res += x * i;
		if (res >= t) {
			ans = n - i + 1;
			break;
		}

	}
	cout << ans << endl;

}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

