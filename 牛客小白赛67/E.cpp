const int N = 1000010;
int n,m;
void solve(){
	cin >> n;
	vector<double> a(n),b(n);

	for(int i = 0 ; i < n ; i++) cin >> a[i];
	for(int i = 0 ; i < n ; i++) cin >> b[i];

	vector<double> f(n);
	f[n - 1] = 0.5 * a[n - 1] + 0.5 * b[n - 1];

	for(int i = n - 2 ; i >= 0 ; i--)
		f[i] = 0.5 * min(a[i],f[i + 1]) + 0.5 * min(b[i],f[i + 1]);

	cout << fixed << setprecision(6) << f[0] << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	cin >> T;
	while(T--) solve();
	return 0;
}

