const int N = 1000010;
int n,m;
void solve(){
	cin >> n >> m;
	vector<int> st(n);
	int ans = 0;
	while(m--){
		int op,num;
		cin >> op >> num;
		num--;
		if(op == 1) {
			if(num - 3 >= 0) {
				st[num - 3] ++;
				if(st[num - 3] == 1) ans++;
			}
			if(num + 3 < n) {
				st[num + 3] ++;
				if(st[num + 3] == 1) ans++;
			}

		}else{
			if(num - 3 >= 0){
				st[num - 3] --;
				if(!st[num - 3]) ans--;
			} 
			if(num + 3 < n) {
				st[num + 3] --;
				if(!st[num + 3]) ans--;
			}
		}

		cout << ans << endl;
	}
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

