int n,m;
void solve(){
	double xb,xc,yc;
	cin >> xb >> xc >> yc;

	double sm = xb * yc / 2;
	sm /= 2;

	int ans = 0;


	for(int i = 0 ; i <= xc ; i++){
		double now = xc * yc / 2;
		now /= xc * xc;
		now *= 1.0 * i * i;
		if(abs(now - sm) <= 1e-9) ans = 1; 
	}

	for(int i = xb ; i >= xc ; i--){
		double now = (xb - xc) * yc / 2;
		now /= 1.0 * (xb - xc) * (xb - xc);
		now *= 1.0 * (xb - i) * (xb - i);
		if(abs(now - sm) <= 1e-9) ans = 1; 
	}

	if(ans) cout << "YES" << endl;
	else cout << "NO" << endl;

}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	cin >> T;
	while(T--) solve();
	return 0;
}

