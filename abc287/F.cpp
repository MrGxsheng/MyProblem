int n,m;
constexpr int P = 998244353;
using i64 = long long;
// assume -P <= x < 2P
int norm(int x) {
    if (x < 0) {
        x += P;
    }
    if (x >= P) {
        x -= P;
    }
    return x;
}
template<class T>
T power(T a, i64 b) {
    T res = 1;
    for (; b; b /= 2, a *= a) {
        if (b % 2) {
            res *= a;
        }
    }
    return res;
}
struct Z {
    int x;
    Z(int x = 0) : x(norm(x)) {}
    Z(i64 x) : x(norm(x % P)) {}
    int val() const {
        return x;
    }
    Z operator-() const {
        return Z(norm(P - x));
    }
    Z inv() const {
        assert(x != 0);
        return power(*this, P - 2);
    }
    Z &operator*=(const Z &rhs) {
        x = i64(x) * rhs.x % P;
        return *this;
    }
    Z &operator+=(const Z &rhs) {
        x = norm(x + rhs.x);
        return *this;
    }
    Z &operator-=(const Z &rhs) {
        x = norm(x - rhs.x);
        return *this;
    }
    Z &operator/=(const Z &rhs) {
        return *this *= rhs.inv();
    }
    friend Z operator*(const Z &lhs, const Z &rhs) {
        Z res = lhs;
        res *= rhs;
        return res;
    }
    friend Z operator+(const Z &lhs, const Z &rhs) {
        Z res = lhs;
        res += rhs;
        return res;
    }
    friend Z operator-(const Z &lhs, const Z &rhs) {
        Z res = lhs;
        res -= rhs;
        return res;
    }
    friend Z operator/(const Z &lhs, const Z &rhs) {
        Z res = lhs;
        res /= rhs;
        return res;
    }
    friend std::istream &operator>>(std::istream &is, Z &a) {
        i64 v;
        is >> v;
        a = Z(v);
        return is;
    }
    friend std::ostream &operator<<(std::ostream &os, const Z &a) {
        return os << a.val();
    }
};

void solve(){
	cin >> n;
	vector<std::vector<int>> adj(n);
	for(int i = 1 ; i < n ; i++){
		int u,v;
		cin >> u >> v;
		u--,v--;
		adj[u].push_back(v);
		adj[v].push_back(u);
	}

	vector dp(n,vector(n + 1 , array<Z,2>{}));
	vector<int> siz(n);

	auto dfs = [&](auto self , int x ,int p) -> void{
		siz[x] = 1;
		dp[x][0][0] = dp[x][0][1] = 1;
		for(auto y : adj[x]){
			if(y == p) continue;
			self(self,y,x);
			vector g(n + 1 , array<Z,2>{});
			for(int i = 0 ; i <= siz[x] ; i++){
				for(int j = 0 ; j <= siz[y] ; j++){
					g[i + j][0] += dp[x][i][0] * dp[y][j][0];
					if(i + j < n){
						g[i + j + 1][0] += dp[x][i][0] * dp[y][j][1];
					}
					g[i + j][1] += dp[x][i][1] * dp[y][j][0];
					g[i + j][1] += dp[x][i][1] * dp[y][j][1];
				}
			}

			siz[x] += siz[y];
			swap(dp[x],g);
		}
	};

    dfs(dfs,0,-1);

	vector<Z> ans(n + 1);
	for(int i = 0 ; i <= n ; i++){
		ans[i] += dp[0][i][0];
		if(i < n){
			ans[i + 1] += dp[0][i][1];
		}
	}

	for(int i = 1 ; i <= n ; i++){
		cout << ans[i] << endl;
	}
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

