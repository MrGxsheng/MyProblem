const int N = 1000010;
int n,m;
int son[N][26], cnt[N], idx;
string s[N];
void insert(const char *s)
{
    int p = 0;
    for (int i = 0; s[i]; i ++ )
    {
        int u = s[i] - 'a';
        if (!son[p][u]) son[p][u] = ++ idx;
        p = son[p][u];
        cnt[p] ++ ;

    }
}

int query(const char *s)
{
    int p = 0,res= 0;
    for (int i = 0; s[i]; i ++ )
    {
        int u = s[i] - 'a';
        p = son[p][u];
        if(cnt[p] == 1) return res;
        res++;
    }
    return res;
}

void solve(){
	cin >> n;
	for(int i = 0 ; i < n ; i++) {
		cin >> s[i];
		insert(s[i].c_str());
	}

	for(int i = 0; i < n ; i++){
		cout << query(s[i].c_str()) << endl;
	}
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

