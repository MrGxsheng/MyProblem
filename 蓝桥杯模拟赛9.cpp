const int N = 1000010;
string mp[105];
int n, m;
int ans;

void is_X(int x, int y) {
    char c = mp[x][y];
    for (int i = 1; i < 200; ++i) {
        if( !(x - i >= 0 && x + i < n && y - i >= 0 && y + i < m && 
            c == mp[x - i][y - i] && c == mp[x + i][y + i] && 
            c == mp[x - i][y + i] && c == mp[x + i][y - i] )){
            ans += i - 1;
            break;
        }

    }
}

void solve(){
	cin >> n >> m;
    for (int i = 0; i < n; ++i) {
        cin >> mp[i];
    }
    for (int i = 1; i < n - 1; ++i) {
        for (int j = 1; j < m - 1; ++j) {
            is_X(i, j);
        }
    }
    cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

