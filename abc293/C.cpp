typedef long long LL;
typedef pair<int,int> PII;
const int N = 20;
int n,m;
int a[N][N];
int ans;
set<int> s;
void dfs(int x ,int y){
	if(s.count(a[x][y])) return;
	s.insert(a[x][y]);
	if(x == n - 1 && y == m - 1){
		ans++;
		s.erase(a[x][y]);
		return;
	}

	if(x < n - 1) dfs(x + 1,y);
	if(y < m - 1) dfs(x,y + 1);
	s.erase(a[x][y]);
}

void solve(){
	cin >> n >> m;
	for(int i = 0 ; i < n ; i++)
		for(int j = 0 ; j < m ; j++)
			cin >> a[i][j];

	dfs(0,0);
	cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

