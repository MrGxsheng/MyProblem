using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m;
void solve(){
	cin >> n >> m;
	vector<int> c(n);
	for(int i = 0 ; i < n ; i++) cin >> c[i];

	vector<vector<int>> adj(n);
	for(int i = 0 ; i < m ; i++) {
		int u,v;
		cin >> u >> v;
		u--,v--;
		adj[u].push_back(v);
		adj[v].push_back(u);
	}

	vector dp(n,vector<int>(n , -1));
	queue<tuple<int,int,int>> q;
	q.emplace(0,0,n - 1);

	while(!q.empty()){
		auto [d,x,y] = q.front();
		q.pop();

		if(dp[x][y] != -1) continue;
		dp[x][y] = d;
		for(auto a : adj[x]){
			for(auto b : adj[y]){
				if(c[a] != c[b]){
					q.emplace(d + 1,a ,b);
				}
			}
		} 
	}

	cout << dp[n - 1][0] << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	cin >> T;
	while(T--) solve();
	return 0;
}

