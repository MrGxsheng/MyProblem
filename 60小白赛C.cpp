const int N = 1000010;
int n,m;
int a[N];
int f[N];
void solve(){
	cin >> n >> m;
	for(int i = 1 ; i <= n ; i++) cin >> a[i];

	int res = 0;
	for(int i = 1 ; i <= n ; i++){
		f[i] = a[i];
		for(int j = 1 ; j < i - m ; i++)
			f[i] = max(f[i],f[j] + a[i]);
		res = max(res,f[n]);
	}
	cout << f[n] << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

