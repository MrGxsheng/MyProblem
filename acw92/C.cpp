const int N = 1010;
struct DSU {
    vector<int> f, siz;
    DSU(int n) : f(n), siz(n, 1) { iota(f.begin(), f.end(), 0); }
    int leader(int x) {
        while (x != f[x]) x = f[x] = f[f[x]];
        return x;
    }
    bool same(int x, int y) { return leader(x) == leader(y); }
    bool merge(int x, int y) {
        x = leader(x);
        y = leader(y);
        if (x == y) return false;
        siz[x] += siz[y];
        f[y] = x;
        return true;
    }
    int size(int x) { return siz[leader(x)]; }
};

int n,m;
int st[N][N];
int maxx;
void solve(){
	int n,m;
	cin >> n >> m;
	DSU dsu(n);
	int cnt = 0;
	while(m--){
		vector<int> v;
		int x,y;
		cin >> x >> y;
		x--,y--;
		if(!dsu.merge(x,y)) cnt++;

		for(int i = 0 ; i < n ; i++){
			if(i == dsu.leader(i)){
				v.push_back(dsu.size(i));
			}
		}

		sort(v.begin(), v.end(),greater<int>());
		int ans = 0;
		for(int i = 0 ; i < v.size() && i <= cnt ; i++){
			ans += v[i];
		}
		cout << ans - 1<< endl;
	}
}

