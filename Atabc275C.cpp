const int N = 1000010;
int n,m;
string s[N];
bool check(int x, int y) {
    return x>=0 and x<=8 and y>=0 and y<=8 and s[x][y] == '#';
}
void solve(){
    for (int i = 0; i < 9; i++)
        cin >> s[i];
   	int ans = 0;
    for (int i = 0; i < 9; i++) {
        for (int j = 0; j < 9; j++) {
            for (int dx = -8; dx < 9; dx++) {
                for (int dy = -8; dy < 9; dy++) {
                    if(!dx and !dy) continue;
                    int i2 = i + dx, j2 = j + dy;
                    int i3 = i2 - dy, j3 = j2 + dx;
                    int i4 = i3 - dx, j4 = j3 - dy;
                    if (check(i, j) and check(i2, j2) and check(i3, j3) and check(i4, j4)) {
                        ans ++;
                    }
                }
            }
        }
    }
    cout << ans / 4 << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

