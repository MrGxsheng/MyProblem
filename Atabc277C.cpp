using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010 , M = N;
int n,m,idx;
int ans;
map<int,int> mp,h,e,ne;
void add(int a,int b){
	idx++;
	e[idx] = b;
	ne[idx] = h[a];
	h[a] = idx;
}

void dfs(int x){
	if(mp[x] != 0) return;
	mp[x] = 1;
	ans = max(ans,x);

	for(int i = h[x] ; i ; i = ne[i]){
		int j = e[i];
		dfs(j);
	}
}

void solve(){
	cin >> n;
	
	for(int i = 1 ; i <= n ; i++){
		int a,b;
		cin >> a >> b;
		add(a,b);
		add(b,a);
	}

	dfs(1);

	cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

