const int N = 1000010;
int n,m;
void solve(){
	cin >> n >> m;
	vector<vector<int>> adj(n);
	for(int i = 0 ; i < m ; i++){
		int u,v;
		cin >> u >> v;
		u--,v--;
		adj[u].push_back(v);
	}

	int ans = 0;
	for(int i = 0 ; i < n ; i++){
		queue<int> q;
		q.push(i);
		vector<bool> vis(n);
		vis[i] = true;

		while(q.size()){
			int x = q.front();
			q.pop();
			ans ++;
			for(auto y : adj[x]){
				if(!vis[y]){
					vis[y] = true;
					q.push(y);
				}
			}
		}


	}
	ans -= n + m;
	cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

