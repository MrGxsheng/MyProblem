using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,k;
void solve(){
	cin >> n >> k;
	vector<LL> a(n);
	vector<LL> s(n);
	for(int i = 0 ; i < n ; i++) {
		cin >> a[i];
		s[i] = a[i];
		if(i >= k){
			s[i] += s[i - k];
		}
	}

	int T;
	cin >> T;
	while(T--){
		int x,y;
		cin >> x >> y;
		x--,y--;
		vector<LL> b(k);
		for(int i = y ; y - i + 1 <= k ; i--) b[i % k] += s[i];
		for(int i = x - 1 ; i >= 0 && x - i <= k ; i--) b[i % k] -= s[i];
		if(b == vector(k,b[0])) cout << "Yes" << endl;
		else cout << "No" << endl;
	}

}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

