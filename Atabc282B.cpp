using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 10010;
int n,m;
string s[N];
vector<int> v[N];
void solve(){
	cin >> n >> m;
	for(int i = 0 ; i < n ; i++) cin >> s[i];
	for(int i = 0 ; i < n ; i++)
		for(int j = 0 ; j < m ; j++)
			if(s[i][j] == 'x')
				v[i].push_back(j + 1);

	int ans = 0;
	for(int i = 0 ; i < n ; i++)
		for(int j = i + 1 ; j < n ; j++){
			bool st = true;
			for(auto a : v[i]){
				for(auto b : v[j]){
					if(a == b) {
						st = false;
						break;
					} 
				}
				if(!st) break;
			}
			if(st) ans++;
		}
	cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

