#include <bits/stdc++.h>
#define debug puts("-----")
#define eps (1e-8)
#define inf (1<<30)
#define fi first
#define se second
#define endl '\n'
using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m,q;
vector<PII> edge;
vector<int> g[N];
int k, deg[N], vis[N];
void solve(){
	cin >> n >> m >> q;

	for(int i = 1 ; i <= m ; i++){
		int u,v;
		cin >> u >> v;
		deg[u] ++;
		deg[v] ++;
		edge.push_back({u,v});
	}

	for(auto ed : edge){
		int u = ed.fi , v = ed.se;
		if(deg[u] < deg[v]){
			g[u].push_back(v);
		}else{
			g[v].push_back(u);
		}
	}

	while(q--){
		cin >> k;
		vector<int> node(k);
		for(int &x : node) cin >> x;
		for(int u : node) vis[u] = 1;

		int ans = 0;
		for(int u : node){
			for(int v : g[u])
				if(vis[v]) ans++;
		}
		cout << ans << endl;
		for(int u : node) vis[u] = 0;
	}

}	

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

