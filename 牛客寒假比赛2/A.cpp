void solve(){
	cin >> n;
	int l1,r1,l2,r2;
	cin >> l1 >> r1 >> l2 >> r2;

	if(n < l1 + l2 || n > r1 + r2){
		cout << 0 << endl;
		return;
	}
	LL ans = 0;
	int x = n - l1;
	int z = 0;
	if(x > r2) z = x - r2;
	
	ans += min((x - l2),(r1 - l1)) + 1;
	

	cout << ans - z << endl;


}

