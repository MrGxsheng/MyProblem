void solve(){
	string s;
	cin >> s;
	n = s.length();
	stack<int> left;
	set<char> box;
	stack<pair<char,int>> st;
	for(int i = 0 ; i < n ; i++){
		char c = s[i];
		if(c == '(') left.push(i);
		else if(c == ')'){
			int l = left.top();
			left.pop();
			while(!st.empty() && st.top().second > l){
				char curr = st.top().fi;
				box.erase(curr);
				st.pop();
			}
		}else {
			if(box.count(c)){
				cout << "No" << endl;
				return;
			}else {
				box.insert(c);
				st.push({c,i});
			}

		}
	}

	cout << "Yes" << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

