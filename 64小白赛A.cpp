const int N = 1000010;
LL n,k,a,b;
void solve(){
	cin >> n >> k >> a >> b;
	LL x = (k - 1) * b + (n - 1) * b;
	LL y = (n - 1) * a;
	if(x == y) cout << "0" << endl;
	else if(x > y) cout << '2' << endl;
	else cout << "1" << endl;
}

