using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m;

struct DSU {
    vector<int> f, siz;
    DSU(int n) : f(n), siz(n, 1) { iota(f.begin(), f.end(), 0); }
    int leader(int x) {
        while (x != f[x]) x = f[x] = f[f[x]];
        return x;
    }
    bool same(int x, int y) { return leader(x) == leader(y); }
    bool merge(int x, int y) {
        x = leader(x);
        y = leader(y);
        if (x == y) return false;
        siz[x] += siz[y];
        f[y] = x;
        return true;
    }
    int size(int x) { return siz[leader(x)]; }
};

void solve(){
	cin >> n >> m;
	DSU dsu(n);
	for(int i = 0 ; i < m ; i++){
		int u,v;
		cin >> u >> v;
		u-- , v--;
		dsu.merge(u,v);
	}

	int comp = 0;
	vector<int> s(n);
	for(int i = 0 ; i < n ; i++){
		int c;
		cin >> c;
		if(c){
			int x = dsu.leader(i);
			comp += !s[x];
			s[x] += c;
		}
	}

	LL ans = 0;
	for(int i = 0 ; i < n ; i++){
		if(dsu.leader(i) == i){
			if(comp - (s[i] > 0) == 0){
				int s = dsu.size(i);
				ans += 1LL * s * s;
			}
		}
	}
	cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

