const int N = 1000010;
int n,m;
int primes[N];
int cnt;
bool st[N];

void get_primes(int n)
{
    for (int i = 2; i <= n; i ++ )
    {
        if (st[i]) continue;
        primes[++ cnt] = i;
        for (int j = i + i; j <= n; j += i)
            st[j] = true;
    }
}

int f[4500];
void solve(){

	get_primes(2022);
	memset(f,-0x3f,sizeof f);
	f[0] = 0;

	for(int i = 1 ; i <= cnt ; i ++)
		for(int j = 2022 ; j >= primes[i] ; j--)
			f[j] = max(f[j],f[j - primes[i]] + 1);
		
	cout << f[2022] << endl; //33

}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

