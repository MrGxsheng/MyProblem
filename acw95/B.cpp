const int N = 1000010;
int n,m;
int f[N];

void init(){
	f[1] = 1;
	for(int i = 2 ; i <= N ; i++)
		if(!f[i])
			for(int j = 2 ; j * i <= N ; j++)
				f[i * j] = 1;
}

void solve(){
	init();
	cin >> n;
	vector<LL> a(n);
	for(int i = 0 ; i < n ; i++) cin >> a[i];


	for(int i = 0 ; i < n ; i++){
		LL sq = sqrtl(a[i]);
		if(sq * sq == a[i]){
			if(!f[sq]) cout << "YES" << endl;
			else cout << "NO" << endl;
		}else cout << "NO" << endl;
	}
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

