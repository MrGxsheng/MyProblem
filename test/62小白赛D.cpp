using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
LL n,m,k;

LL dfs(LL l,LL r){
	if(l >= n) return 0;
	r = min(r,n - 1);
	LL res = r - l + 1;
	res += dfs(l * k + 1 ,(r + 1) * k);
	return res;

}

void solve(){
	cin >> n >> k >> m;
	while(m--){
		LL x;
		cin >> x;
		if(k == 1){
			cout << n - x << endl;
			continue;
		}
		cout << dfs(x,x)<< endl;
	}

}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	cin >> T;
	while(T--) solve();
	return 0;
}

