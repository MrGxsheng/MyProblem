#include <bits/stdc++.h>
#define debug puts("-----")
#define eps (1e-8)
#define inf (1<<30)
#define fi first
#define se second
#define endl '\n'
using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m;
void solve(){
	cin >> n;
	vector<int> a(n);
	iota(a.begin(),a.end(),1);

	vector<vector<PII>> ans;
	while(!is_sorted(a.begin() , a.end() , greater<int>())){
		vector<PII> s;
		for(int i = 1 ; i < n ; i++){
			if(a[i] > a[i - 1]) {
				swap(a[i] , a[i - 1]);
				s.emplace_back(a[i],a[i - 1]);
				i++;
			}
		}
		ans.push_back(s);
	}

	cout << ans.size() << endl;
	for(auto v : ans){
		cout << v.size() << endl;
		for(auto [x,y] : v){
			cout << x << ' ' << y << endl;
		}
	}
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

