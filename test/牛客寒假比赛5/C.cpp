void solve(){
	cin >> a >> b;
	bool f = 0;
	if(a == b){
		cout << "=" << endl;
		return;
	}
	if(a.size() == b.size()){
		cout << "!" << endl;
		return;
	}

	if(a.size() < b.size()){
		f = 1;
		swap(a,b);
	}

	int c = a.size() - b.size();
	for(int i = 0 ; i < c ; i++){
		if(a[i] != a[0]){
			if(f) cout << "<" << endl;
			else cout << ">" << endl;
			return;
		}
	}

	for(int i = 0 ; i < b.size() ; i ++)
		if(b[i] != a[i + c]){
			if(b[i] == a[0]){
				if(f) cout << "<" << endl;
				else cout << ">" << endl;
				return;
			}
			break;
		}
		cout << "!" << endl;

}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

