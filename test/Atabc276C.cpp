void solve(){
	cin >> n;
	for(int i = 1 ;  i <= n ; i++ ) cin >> a[i];
	
	for(int i = n  ; i >= 1 ; i --){
		vector<int > nums;
		int mi = -1e9;
		for(int j = i + 1 ; j <= n ; j++){
			if(a[i] > a[j]) mi = max(mi,a[j]);
			nums.push_back(a[j]);
		}

		if(mi != -1e9){
			nums.push_back(a[i]);
			sort(nums.rbegin() , nums.rend());
			for(int j = 1 ; j < i ; j++) cout << a[j] << ' ';
			cout << mi << ' ';
			for(auto x : nums)
				if(x != mi)
					cout << x << ' ';
			break;
		}
	}
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

