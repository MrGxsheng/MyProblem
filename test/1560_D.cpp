using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m;
LL f[110];
void init(){
	f[0] = 1;
	for(int i = 1; i <= 55 ; i++) f[i] = f[i - 1] * 2;
}

void solve(){
	string s;

	cin >> s;
	n = s.length();
	int ans = 100;
	for(int i = 0 ; i <= 55; i++){
		string ss = to_string(f[i]);
		m = ss.length();
		int res = 0;
		int j = 0 , k = 0;
		for(j = 0 , k = 0; j < n && k < m ; ){
			if(s[j] != ss[k]) j++,res++;
			else j++ , k++;
		}
		if(k == m)res += n - j;
		else if(j == n) res += m - k;
		ans = min(res,ans);
	}
	cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	init();
	cin >> T;
	while(T--) solve();
	return 0;
}

