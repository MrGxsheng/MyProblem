using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m;
int a[N];
void solve(){
	cin >> n;
	int ans = 0;
	bool flag = false;
	int allgcd = 0;
	for(int i = 1 ; i <= n ; i++){
		cin >> a[i];
		allgcd = __gcd(allgcd,a[i]);
	}
	for(int i = 1 ; i <= n ; i++){
		a[i] /= allgcd;

		while(a[i] % 2 == 0){
			a[i] /= 2;
			ans++;
		}

		while(a[i] % 3 == 0){
			a[i] /= 3;
			ans++;
		}

		if(a[i] != 1){
			flag = true;
			break;
		}
	}


	if(!flag) cout << ans << endl;
	else cout << -1 << endl;

}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

