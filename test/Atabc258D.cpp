using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 200010;
LL n,m;
LL a[N];
LL b[N];
LL s[N];

void solve(){
	cin >> n >> m;
	for(int i = 1 ; i <= n ; i++) {
		cin >> a[i] >> b[i];
		s[i] = s[i - 1] + a[i] + b[i];
	}

	LL res = 1e22;
	LL ans = 0;
	for(int i = 1 ; i <= n ; i ++){
		if(m >= i) ans = s[i] + b[i] * (m - i);
		res = min(res,ans);
		
	}
	cout << res << endl;
}
int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

