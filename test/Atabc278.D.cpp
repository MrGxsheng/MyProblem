using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m;
map<LL,LL> mp;
void solve(){
	cin >> n;
	vector<LL> a(n + 1);
	for(int i = 1 ; i <= n ; i++) cin >> a[i];
	cin >> m;

	int res = 0;
	LL num;
	while(m--){
		int op,x,y;
		

		cin >> op;
		if(op == 1) {
			cin >> num;
			res++;
		}else if(op == 2){
			cin >> x >> y;

			if(mp[x] == res) a[x] += y;
			else {
				mp[x] = res;
				a[x] = num + y;
			}

		}else {
			cin >> x;
			if(mp[x] != res) cout << num << endl;
			else cout << a[x] << endl;
		}
	}
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

