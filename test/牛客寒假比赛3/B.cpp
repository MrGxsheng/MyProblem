LL n,m;
void solve(){
	cin >> n;
	if(n == 2) cout << -1 << endl;
	else {
		LL l = 0 , r = 1e18;
		while(l < r){
			LL mid = l + r + 1>> 1;
			if(mid + 2 * (mid - (n + 1) / 2) <= n) l = mid;
			else r = mid - 1;
		}
		if(l == 0) cout << -1 << endl;
		else cout << l << endl;

	}	
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	cin >> T;
	while(T--) solve();
	return 0;
}

