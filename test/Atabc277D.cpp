#define endl '\n'
using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
LL n,m;
LL a[N]; 

void solve(){
	LL n,m,sum=0;
	cin>>n>>m;
	for (int i=1;i<=n;i++)
	{
		cin>>a[i];
		sum+=a[i];
	}
	sort(a+1,a+1+n);
	for (int i=n+1;i<=2*n;i++) a[i]=a[i-n]+m;
	LL l=1,r=1;
	LL now=0,ans=sum,cnt=0;
	while(l<=n*2)
	{
		while(r<=2*n&&(l==r||a[r]==a[r-1]||a[r]==a[r-1]+1)&&cnt<n)
		{
			now+=a[r]>=m?a[r]-m:a[r];
			r++;
			cnt++;
		}
		ans=min(ans,sum-now);
		now-=a[l]>=m?a[l]-m:a[l];
		l++;
		cnt--;
	}
	cout<<ans<<endl;
	return;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

