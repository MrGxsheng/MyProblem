using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m,k;
void solve(){
	cin >> n >> m >> k;
	LL ans = 0;
	bool ok = true;
	for(int i = 1 ; i <= n ; i++){
		int a,b;
		cin >> a >> b;
		if(a > k && b >= k){
			ok = false;
			break;
		} 

		if(a <= k) ans++;
		else ans += (a + k - b - 1) / (k - b);
	}

	if(ok) cout << -1 << endl;
	else cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

