#include <bits/stdc++.h>
#define debug puts("-----")
#define eps (1e-8)
#define inf (1<<30)
#define fi first
#define se second
using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m;
map<PII,int > mp;
PII a[N];
void solve(){
	cin >> n;

	for(int i = 0 ; i < n ; i++) {
		cin >> a[i].fi >> a[i].se;
		mp[{a[i].fi,a[i].se}] = 1;
	}

	sort(a,a + n);
	int ans = 0;
	for(int i = 0 ; i < n ; i++)
		for(int j = i + 1 ; j < n ; j++){
			int x1 = a[i].fi , y1 = a[i].se;
			int x2 = a[j].fi , y2 = a[j].se;
			if(x1 >= x2 || y1 >= y2) continue;
			// cout << x1 << ' ' << y1 << ' ' << x2 << ' ' << y2 << endl;
			if(mp[{x1,y2}] && mp[{x2,y1}]) ans++;
		}

	cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

