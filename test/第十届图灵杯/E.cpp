const int N = 1000010;
int n,m;
int a,b,c;
int ans;
int x[11],y[11],z[11],w[11];

void dfs(int u ,int q , int qq , int qqq , int res){
	if(u == n + 1){
		if(q <= a && qq <= b && qqq <= c){
			ans = max(ans,res);
		}
		return;
	}

	dfs(u + 1 , q + x[u] , qq + y[u] , qqq + z[u] , res + w[u]);
	dfs(u + 1 , q , qq ,qqq , res);
}

void solve(){
	cin >> n >> a >> b >> c;
	for(int i = 1 ; i <= n ; i++){
		cin >> x[i] >> y[i] >> z[i] >> w[i];
	}

	dfs(1,0,0,0,0);

	cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

