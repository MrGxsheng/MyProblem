#include <bits/stdc++.h>
#define debug puts("-----")
#define eps (1e-8)
#define inf (1<<30)
#define fi first
#define se second
#define endl '\n'
using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m;
LL a[N];
LL b[N];
LL c[N];
LL s[N];
void solve(){
	cin >> n;
	map<int,int> mp;
    fill(b,b + N,0);
    fill(c,c + N,0);
    fill(s,s + N,0);
	for(int i = 1 ; i <= n ; i++) {
		cin >> a[i];
		mp[a[i]]++;
    }

	for(auto x : mp){
		c[x.se] ++;
	}

	for(int i = 1 ; i <= 1e5 ; i++) s[i] = s[i - 1] + i * c[i];
	for(int i = 1e5 ; i >= 1 ; i--) b[i] = c[i] + b[i + 1];

	for(int i = 1 ; i <= n ; i++){	
	    cout << s[i] + (i - 1) * b[i + 1] << endl; 
	}
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	cin >> T;
	while(T--) solve();
	return 0;
}

