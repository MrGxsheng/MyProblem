#define se second
#define endl '\n'
using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m;
int dist[N];
vector<int>  f[N];
int w[N];
void dfs(int u){
	
	for(auto x : f[u]){
		dist[x] = dist[u] + 1;
		dfs(x);
	}
}


void solve(){
	cin >> n;
	for(int i = 2 ; i <= n ; i++) {
		int x;
		cin >> x;
		f[x].push_back(i);
	}
	dist[1] = 1;
	dfs(1);

	for(int i = 1 ; i <= n ; i++) cin >> w[i];

	sort(dist + 1 ,dist + n + 1);

	sort(w + 1 , w + 1 + n);
	LL ans = 0;
	for(int i = 1 ; i <= n ; i++) ans += 1ll * dist[i] * w[i];
	cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

