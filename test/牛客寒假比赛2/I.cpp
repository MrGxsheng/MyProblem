#include <bits/stdc++.h>
#define debug puts("-----")
#define eps (1e-8)
#define inf (1<<30)
#define fi first
#define se second
#define endl '\n'
using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m;
void solve(){
	cin >> n;

	vector<int> x(n);
	for(int i = 0 ; i < n ; i++) cin >> x[i];

	int a,b,c,d;
	cin >> a >> b >> c >> d;
	d++;

	array<int,5> v;
	for(int i = 0 ; i < 5 ; i++) cin >> v[i];

	LL res = 0;
	vector<PII> events;
	for(int i = 0 ; i < n ; i++){
		res += v[0];
		events.emplace_back(a - x[i] , v[1] - v[0]);
		events.emplace_back(b - x[i] , v[2] - v[1]);
		events.emplace_back(c - x[i] , v[3] - v[2]);
		events.emplace_back(d - x[i] , v[4] - v[3]);
	}

	LL ans = res;
	sort(events.begin(),events.end());

	for(auto [_,x] : events){
		res += x;
		ans = max(ans,res);
	}
	cout << ans << endl;

}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	cin >> T;
	while(T--) solve();
	return 0;
}

