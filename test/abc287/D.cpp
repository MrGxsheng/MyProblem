int n,m;

bool match(char a, char b){
	return a == '?' || b == '?' || a == b;
}

void solve(){
	string S,T;
	cin >> S >> T;
	n = S.size();
	m = T.size();

	int i = 0 , j = 0;
	while(i < m && match(S[i],T[i])){
		i++;
	}

	while(j < m && match(S[n - 1 - j],T[m - 1 - j])){
		j++;
	}

	for(int x = 0 ; x <= m ; x++){
		cout << (x <= i && m - x <= j ? "Yes" : "No") << endl;
	}

}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

