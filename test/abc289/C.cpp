const int N = 1000010;
int n,m;
void solve(){
	cin >> n >> m;
	vector<int> a(m);

	int cnt = 0;
	for(int i = 0 ; i < m ; i++){
		int x;
		cin >> x;
		for(int j = 0 ; j < x ; j++){
			int y;
			cin >> y;
			y--;
			a[i] |= 1 << y;
		}
	}

	for(int i = 0 ; i < (1 << m) ; i++){
		int res = 0;
		for(int j = 0 ; j < m ; j++){
			if(i >> j & 1) res |= a[j];
		}
 
		if(res == (1 << n) - 1) cnt++;
	}

	cout << cnt << endl;
	
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

