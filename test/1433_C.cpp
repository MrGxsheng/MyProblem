typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m;
int a[N];
void solve(){
	cin >> n;
	int maxx = 0;
	int idx = 0;
	for(int i = 1 ; i <= n ; i++){
		cin >> a[i];
		if(a[i] > maxx) maxx = a[i],idx = i;
	}

	for(int i = 1 ; i <= n ; i++){
		if(a[i] == maxx){
			if(i != 1 && i != n && (a[i - 1] < a[i] || a[i] > a[i + 1])){
				cout << i << endl;
				return;
			}else if(i == 1 && a[i] > a[i + 1]){
				cout << i << endl;
				return;
			}else if(i == n && a[i] > a[i - 1]){
				cout << i << endl;
				return;
			}
		}
	}
	sort(a + 1 , a + 1 + n);
	if(a[1] == a[n]){
		cout << -1 << endl;
		return;
	}
	cout << idx << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	cin >> T;
	while(T--) solve();
	return 0;
}

