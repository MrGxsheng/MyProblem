const int N = 10010;
int n,m;
int f[N];
void solve(){
	 cin >> n >> m;
	 vector<int> v(n),w(n); 
	 for(int i = 0 ; i < n ; i++){
	 	cin >> v[i] >> w[i];
	 }

	 f[0] = 1;
	 for(int i = 0 ; i < n ; i++){
	 	for(int j = m ; j >= v[i] ; j--){
	 		for(int k = 1 ; k * v[i] <= j && k <= w[i] ; k++)
	 			f[j] |= f[j - k * v[i]];
	 	}	
	 }

	 if(f[m]) cout << "Yes" << endl;
	 else cout << "No" << endl;
	 
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

