using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
LL a,b;
long double ans = 1e18;
long double getAns(long double x){
	return x * b + a / (sqrtl(x + 1));
}

void solve(){
	cin >> a >> b;
	cout << fixed << setprecision(15);
	long double l = 0 , r = 1e18;
	while(l <= r){
		LL lmid = (r - l) / 3 + l , rmid = r - (r - l) / 3;
		long double v1 = getAns(lmid) , v2 = getAns(rmid);
		if(v1 >= v2) l = lmid + 1;
		else r = rmid - 1;
		ans = min({ans,v1,v2});
	}
	cout << ans << endl;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

