const int N = 1000010;
int n,m;
int a[N];
void solve(){
	cin >> n;
	for(int i = 1 ; i <= n ; i++) cin >> a[i];

	sort(a + 1 , a + 1 + n);

	int cnt = 0;
	int cnt1 = 0;
	for(int i = 1 ; i <= n ; i++){
		if(a[i] & 1) cnt++;
		else cnt1++;
	}

	if(cnt & 1 && cnt1 & 1) {
		bool st = false;

		for(int i = 1 ; i <= n - 1; i++)
			if(a[i + 1] - a[i] == 1) st = true;
		
		if(st){
			cout << "YES" << endl; 
			return;
		} 
	}

	if(cnt % 2 == 0 && cnt1 % 2 == 0){
		cout << "YES" << endl;
		return;
	}
	cout << "NO" << endl;
	


}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	cin >> T;
	while(T--) solve();
	return 0;
}

