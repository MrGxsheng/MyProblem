#include <bits/stdc++.h>
#define debug puts("-----")
#define eps (1e-8)
#define inf (1<<30)
#define fi first
#define se second
#define endl '\n'
using namespace std;
typedef long long LL;
typedef pair<int,int> PII;
const int N = 1000010;
int n,m;
void solve(){
	cin >> n >> m;
	vector<PII> a(n);
	for(int i = 0 ; i < n ; i++) {
		int x;
		cin >> x;
		a[i] = {min(x + i + 1,x + n - i),x + i + 1};
	}

	sort(a.begin(), a.end());
	vector<LL> sum(n + 1);
	for(int i = 0 ; i < n ; i++) sum[i + 1] = sum[i] + a[i].fi;

	int ans = 0;
	for(int i = 0 ; i < n ; i++){
		if(a[i].se <= m){
			int l = 0 , r = n;
			while(l < r){ 
				int x = (r + l + 1) / 2;
				LL val = sum[x];
				if(x > i){
					val -= a[i].fi;
				}
				if(val + a[i].se <= m) l = x;
				else r = x - 1;
			}
			ans = max(ans,1 + l - (l > i));

		}
	}

	cout << ans << endl;
	
	



}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	cin >> T;
	while(T--) solve();
	return 0;
}

