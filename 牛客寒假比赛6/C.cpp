const int N = 1000010;
int n,m;
int f[1010][1010];
int a[1010];
void solve(){
	cin >> n;
	int pre = 0;
	for(int i = 1 ; i <= n / 2 ; i++){
		a[i] = pre + 1;
		a[n - i + 1] = a[i] + 1;
		pre = a[i] + 1;
	}

	for(int i = 1 ; i <= n ; i++) f[1][i] = a[i];
	for(int i = 2 ; i <= n ; i++)
		for(int j = 1 ; j <= n - i + 1 ; j++)
			f[i][j] = f[i - 1][j] + f[i - 1][j + 1];
		

	cout << f[n][1] << endl;
	for(int i = 1 ; i <= n ; i++) cout << a[i] << ' ';
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0),cout.tie(0);
	int T;
	// cin >> T;
	T = 1;
	while(T--) solve();
	return 0;
}

